#Numeric data type
a=10
print(type(a))
a=10.342
print(type(a))
a=3+3j
print(type(a))

#string datatype
name="srija"
print(type(name))
name='''puritipatisrija'''
print(type(name))

#tuple
t=tuple()
a=(2,3,4,5)
print(t,a)
print(type(t))
print(type(a))

#set
s=set()
a={1,1,1,2,2,3,3,4}
print(s,a)
print(type(s))
print(type(a))

#frozenset
s=frozenset({1,2,3})
print(type(s))

#Dictionary
a={'name':'srija','age':21,'weight':45}
print(type(a))

#boolean
status=True
status=False
type(status)

#null
randomvar=None
type(randomvar)
